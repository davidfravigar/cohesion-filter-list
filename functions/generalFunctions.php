<?php
/**
 * -------------------------------------------------------------------------------------------------
 *
 * -------------------------------------------------------------------------------------------------
 * @param  [type] $d [description]
 * @return [type]    [description]
 * -------------------------------------------------------------------------------------------------
 */
function arrayToObject($d) {
  if (is_array($d)) {
    return (object) array_map(__FUNCTION__, $d);
  }
  else {
    // Return object
    return $d;
  }
}